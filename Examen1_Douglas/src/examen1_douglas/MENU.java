    private void jbEntrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbEntrarActionPerformed
        if(txtUser.getText() == null || "".equals(txtUser.getText())){
            JOptionPane.showMessageDialog(this, "Ingresar un usuario: ");
        }
        if(txtPass.getText() == null || "".equals(txtPass.getText())){
            JOptionPane.showMessageDialog(this, "Ingresar una contrase単a: ");
        }
        Usuario usuario = null;
        for (int i = 0; i < usuarios.size(); i++) {
            if(txtUser.getText().equals(usuarios.get(i).getNombre()) && txtPass.getText().equals(usuarios.get(i).getContrase単a())){
                usuario = usuarios.get(i);
            }
        }
        if(usuario!=null){
            if(usuario.getRol_Usuario()==1){
                this.dispose();
                MenuLibros menuLibros = new MenuLibros();
                menuLibros.usuarios = usuarios;
                menuLibros.setVisible(true);
            } else {
                // mandar a menu de usuarios normales
                  this.dispose();
                MenuUsuarios menuUsuarios = new MenuUsuarios(usuario);
                menuUsuarios.libros = libros;
                menuUsuarios.setVisible(true);
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Ingresar el usuario o contrase単a correctos: ");
        }
    }//GEN-LAST:event_jbEntrarActionPerformed

